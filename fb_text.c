static void draw_char(unsigned char *buffer, int xres, int pixel_size,
                      int x, int y, char ch,
                      unsigned char r, unsigned char g, unsigned char b)
{
  int row, col;
  unsigned char glyph;

  if (ch < FONT_BASE || ch >= FONT_BASE + GLYPHS)
    return;

  for (row = 0; row < FONT_H; row++) {
    glyph = font_data[ch - FONT_BASE][row];
    for (col = 0; col < FONT_W; col++) {
      if (glyph & (0x80 >> col)) {
        int px = x + col;
        int py = y + row;
        buffer[py * xres * pixel_size + px * pixel_size + 2] = r;
        buffer[py * xres * pixel_size + px * pixel_size + 1] = g;
        buffer[py * xres * pixel_size + px * pixel_size + 0] = b;
      }
    }
  }
}

int main(int argc, char **argv)
{
  int fd, width, height, posx, posy;
  struct fb_var_screeninfo info;
  size_t len, pixel_size;
  unsigned char *buffer;
  unsigned int count, start, end, cols, rows, cx, cy;
  struct timeval tv;
  const char *msg = "The quick brown fox jumps over the lazy dog 0123456789";
  int msg_len = strlen(msg);

  fd = getenv("FRAMEBUFFER") ? open(getenv("FRAMEBUFFER"), O_RDWR) : open("/dev/fb0", O_RDWR);
  if (fd == -1) {
    printf("Failed to open Framebuffer device: %m\n");
    return 1;
  }

  ioctl(fd, FBIOGET_VSCREENINFO, &info);

  if (getenv("WIDTH")) width = atoi(getenv("WIDTH"));
  else width = info.xres;
  if (getenv("HEIGHT")) height = atoi(getenv("HEIGHT"));
  else height = info.yres;
  if (getenv("POSX")) posx = atoi(getenv("POSX"));
  else posx = 0;
  if (getenv("POSY")) posy = atoi(getenv("POSY"));
  else posy = 0;

  len = info.xres * info.yres * info.bits_per_pixel / 8;
  pixel_size = info.bits_per_pixel / 8;
  buffer = mmap(NULL, len, PROT_WRITE, MAP_SHARED, fd, 0);
  fb_console_init();

  cols = width / FONT_W;
  rows = height / FONT_H;

  count = 0;
  gettimeofday(&tv, NULL);
  start = tv.tv_sec * 1000 + tv.tv_usec / 1000;

  do {
    /* Fill screen with random characters */
    for (cy = 0; cy < rows; cy++) {
      for (cx = 0; cx < cols; cx++) {
        char ch = msg[rand() % msg_len];
        draw_char(buffer + posy * info.xres * pixel_size + posx * pixel_size,
                  info.xres, pixel_size,
                  cx * FONT_W, cy * FONT_H, ch,
                  rand() % 256, rand() % 256, rand() % 256);
      }
    }

    count++;
    gettimeofday(&tv, NULL);
    end = tv.tv_sec * 1000 + tv.tv_usec / 1000;
  } while (end < (start + 5000));

  printf("Text frame buffer test bench\n");
  printf("Benchmarking text rendering: %d screens in %.2f seconds\n",
         count, (end - start) / 1000.);
  printf("Characters rendered: %d (%.2f chars/second)\n",
         count * cols * rows, count * cols * rows * 1000. / (end - start));

  fb_console_restore();
  munmap(buffer, len);
  close(fd);

  return 0;
}
